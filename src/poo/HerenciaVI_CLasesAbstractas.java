package poo;

public class HerenciaVI_CLasesAbstractas {
	
	
	/*
	 * CLASES ABSTRACTAS
	 * Buscar La clase la que mas cosas puede hacer
	 * LA CLASE QUE HEREDA MAS ES LA CLASE MAS ESPECIALISADA/POTENTE 
	 * YA QUE ES LA QUE TIENE MAS METODOS
	 * 
	 * 
	 * CUANDO UN METODO NO SE PUDE GENERALIZAR PARA TODAS LA CLASES
	 * EL METODO SE DECLARA ABSTRACTO
	 * METODO ABSTRACTO --> public abstract String dameDescripcion(); no necesita llaves
	 * cuando se haga metodos abstractos se esta obligado a declarar la clase abstracta 
	 * 
	 * Clase abstracta --> abstract class Persona{} seguir un patron en la jerarquia del diseño
	 * 
	 * cuando se declaara un metodo abstracto en una clase todas las clases 
	 * que heredan estan obligadas a sobre escribir los metodos abstractos
	 * 
	 * es un patron de diseño en la jerarquia de herencia
	 * 
	 * un metodo abstracto se va a sobre escribir en cada clase 
	 * en la que se hereda el metodo abstracto
	 * 
	 * Una clase abstracta es una clase que sirve como modelo o base para otras clases relacionadas.
	 * No se puede usar directamente para crear objetos, pero sí permite agrupar características y
	 * comportamientos comunes que pueden compartir todas las clases que heredan de ella.
	 * 
	 * 
	 * Un método abstracto es un método que solo tiene un nombre, sin detalles de cómo funciona. 
	 * Esto obliga a que las clases que heredan de la clase abstracta definan su propio 
	 * funcionamiento para ese método
	 * 
	 * 
	 * Las clases abstractas y los métodos abstractos ayudan a organizar el código estableciendo un 
	 * "esqueleto" común para varias clases, y permiten que cada clase específica 
	 * rellene los detalles de cómo funcionan sus propios métodos.
	 * 
	 * 
	 * LAS CLASES ABTRACTAS NECESITAN MNIMO UN METODO ABTRACTO LAS DEMAS PUEDEN SER METODOS NORMALES
	 * 
	 * 
	 */

}
